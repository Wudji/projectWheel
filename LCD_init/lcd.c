/******************************************************************************
      ïŋ―ïŋ―ïŋ―ïŋ―Ëĩïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―
      ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ÝĢïŋ―x1,y1   ïŋ―ïŋ―Ęžïŋ―ïŋ―ïŋ―ïŋ―
                x2,y2   ïŋ―ïŋ―Öđïŋ―ïŋ―ïŋ―ïŋ―
                color   ïŋ―ßĩïŋ―ïŋ―ïŋ―ÉŦ
      ïŋ―ïŋ―ïŋ―ïŋ―Öĩïŋ―ïŋ―  ïŋ―ïŋ―
******************************************************************************/
void LCD_DrawLine(u16 x1,u16 y1,u16 x2,u16 y2,u16 color)
{
	u16 t; 
	int xerr=0,yerr=0,delta_x,delta_y,distance;
	int incx,incy,uRow,uCol;
	delta_x=x2-x1; //ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ― 
	delta_y=y2-y1;
	uRow=x1;//ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―
	uCol=y1;
	if(delta_x>0)incx=1; //ïŋ―ïŋ―ïŋ―Ãĩïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ― 
	else if (delta_x==0)incx=0;//ïŋ―ïŋ―Öąïŋ―ïŋ― 
	else {incx=-1;delta_x=-delta_x;}
	if(delta_y>0)incy=1;
	else if (delta_y==0)incy=0;//ËŪÆ―ïŋ―ïŋ― 
	else {incy=-1;delta_y=-delta_y;}
	if(delta_x>delta_y)distance=delta_x; //ŅĄČĄïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ― 
	else distance=delta_y;
	for(t=0;t<distance+1;t++)
	{
		LCD_DrawPoint(uRow,uCol,color);//ïŋ―ïŋ―ïŋ―ïŋ―
		xerr+=delta_x;
		yerr+=delta_y;
		if(xerr>distance)
		{
			xerr-=distance;
			uRow+=incx;
		}
		if(yerr>distance)
		{
			yerr-=distance;
			uCol+=incy;
		}
	}
}


